@implementation HMGShareViewController


- (void)viewDidLoad
{
    [super viewDidLoad];
    self.EmailShareButton.enabled = NO;
    NSArray *buttonArray = [NSArray arrayWithObjects:self.EmailShareButton,self.FBshareButton,self.CopyURLButton,nil];
